package section1

fun main() {
    // Para a declaração de variáveis, o Kotlin consegue definir o seu tipo
    // com base na sua inicialização. Dessa forma, não é necessário declarar
    // o seu tipo, desde que o Kotlin possa sabê-lo.
    val name: String = "Luis"
    val age: Int = 21
    val weight: Double = 62.3
    val isMarried: Boolean = false
    val genre: Char = 'M'
}